/**
 * Get labels for a given language. Falls back to EN if key is missing.
 *
 * r-i18n-fix: the Proxy previously returned `prop` (the key name itself)
 * as a last-resort fallback when the key was missing from BOTH the current
 * language and the EN fallback. That made `L.missingKey` truthy (containing
 * the literal string "missingKey"), which silently broke every call site
 * written as `L.missingKey || 'Fallback Text'` — the `||` never fired
 * because the left operand was a truthy string. Result: ~20 UI spots
 * across the app were displaying raw camelCase key names instead of the
 * intended English fallback text (e.g. "specialOrders" header instead of
 * "Special Orders"; "+ newOrder" button instead of "+ New Order").
 *
 * Returning `undefined` for truly-missing keys restores the expected
 * behavior: call sites with `|| 'Fallback'` now render the fallback,
 * and call sites without a fallback will render `undefined` (visible
 * bug, but at least loud — not silent like the key-name-as-label hack).
 */
export function getLabels(lang: Lang): Record<string, any> {
  const en = LABELS.en;
  const target = LABELS[lang] || en;
  // Proxy that falls back to EN for missing keys, or undefined if neither has it
  return new Proxy(target, {
    get(obj: Record<string, LabelValue>, prop: string) {
      return obj[prop] ?? en[prop];
    },
  });
}
